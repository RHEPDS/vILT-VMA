def risk_label_i18n(locale: str, server_label: str) -> str:
    """Map English Low/Medium/High from analysis to localized label."""
    sl = (server_label or "").strip().lower()
    if sl == "high":
        return translate(locale, "risk_high")
    if sl == "medium":
        return translate(locale, "risk_medium")
    if sl == "low":
        return translate(locale, "risk_low")
    return server_label or ""
