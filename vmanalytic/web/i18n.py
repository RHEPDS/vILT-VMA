def pick_locale(request) -> str:
    c = (request.cookies.get(COOKIE_NAME) or "").lower().strip()
    if c in LOCALES:
        return c
    accept = request.headers.get("Accept-Language", "")
    for part in accept.split(","):
        tag = part.split(";")[0].strip().lower()[:2]
        if tag in LOCALES:
            return tag
    return "en"


def _fmt(s: str, kwargs: dict[str, Any]) -> str:
    if not kwargs:
        return s
    try:
        return s.format(**kwargs)
    except (KeyError, ValueError):
        return s


def translate(locale: str, key: str, **kwargs: Any) -> str:
    loc = locale if locale in LOCALES else "en"
    bucket = TRANSLATIONS.get(loc) or TRANSLATIONS["en"]
    s = bucket.get(key) or TRANSLATIONS["en"].get(key) or key
    return _fmt(s, kwargs)


